/**********************************************************
* Function Name: lcd_print_stringXY
**********************************************************
* Summary: prints a string to the LCD screen at a specified
* XY location in specified foreground and background colors
* X will specify the number of characters across with X=0
* being the left most character position, and X=13 being
* the right most.  Y will specify the number of characters
* down the screen with Y=0 being the top of the screen and
* Y=19 being the bottom row of characters.  NOTE this is
* opposite of how columns and rows are specified for the
* IL9341.
* Returns:
*  Nothing
**********************************************************/
void lcd_print_stringXY(
    char *msg, 
    int8_t X,
		int8_t Y,
    uint16_t fg_color, 
    uint16_t bg_color
)
{
	int16_t rows = X;				// saved the index to check if the first char of a line is a space
	int table_index = 0;		// index used to loop up each char in look up table
	int16_t x_start = ROWS - (X* FONT_WIDTH) - FONT_WIDTH;  //  x coordinate to draw 
	int16_t y_start = COLS - (Y * FONT_HEIGHT) - FONT_HEIGHT;	// y coordinate to draw 
	while(*msg != NULL){
		// if there is a space at the beginning of a line, skip the space character
			if( rows == X && *msg == ' ')
				msg++;
			// convert each char into a number that can be used to loop up it from the table
			table_index = ((int)*msg - 32) * 48;
			// draw the char
			lcd_draw_image(x_start,FONT_WIDTH, y_start, FONT_HEIGHT, &sitkaSmall_12ptBitmaps[table_index], fg_color, bg_color);
			// next char
			msg++;
			// move x coordinate
			x_start = x_start - FONT_WIDTH;
			// change rows to indicate that it is not the beginning of a line anymore
			rows++;
			// when the end of a line is reached
			if(x_start < 0){
				// start on the next row in column X
				x_start = ROWS - (X* FONT_WIDTH) - FONT_WIDTH;
				y_start -= FONT_HEIGHT;
				// reset the rows to indicate the beginning of a new line
				rows = X;
				// when the last line of the display is reached
				if(y_start < 0){
					// wrap around to the first line
					y_start = COLS - FONT_HEIGHT;
				}
			}	
	}
}

